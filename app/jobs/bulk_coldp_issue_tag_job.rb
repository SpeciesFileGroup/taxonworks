  def perform(project_id, dataset_id, user_id, issue_keys: nil)
    Current.user_id = user_id
    Current.project_id = project_id

    @user_id = user_id
    @project_id = project_id

    vocab = fetch_vocab
    target_issues = issue_keys&.map { |k| k.to_s.tr('_', ' ').downcase }&.to_set

    # Pre-load valid IDs for fast membership checks (one query each)
    @valid_otu_ids = Otu.where(project_id: project_id).pluck(:id).to_set
    @valid_taxon_name_ids = TaxonName.where(project_id: project_id).pluck(:id).to_set

    keyword_cache = {}
    @cleared_keyword_ids = Set.new # Track which keywords have had their tags deleted
    import_duplicates = false
    pending_tags = [] # Array of {keyword_id:, tag_object_type:, tag_object_id:}

    issues_data = fetch_issues_tsv(dataset_id)

    if issues_data.blank?
      Rails.logger.info("[BulkColdpIssueTagJob] No issues data returned for dataset #{dataset_id}")
      return
    end

    lines = issues_data.lines
    Rails.logger.info("[BulkColdpIssueTagJob] Parsing #{lines.count} lines for dataset #{dataset_id}")

    # Skip header line; parse each data line manually since the TSV
    # has fewer data columns than declared headers
    lines.drop(1).each do |line|
      fields = line.strip.split("\t", -1)
      raw_id = fields[0]
      raw_issues = fields[1]

      next if raw_id.blank? || raw_issues.blank?

      model_class, tw_id = resolve_record(raw_id)
      next unless model_class && tw_id
      next unless valid_id?(model_class, tw_id)

      raw_issues.split(';').each do |raw_issue_key|
        issue_key = raw_issue_key.strip.tr('_', ' ').downcase
        next if issue_key.blank?
        next if target_issues && !target_issues.include?(issue_key)

        import_duplicates = true if issue_key == 'duplicate name'

        keyword = keyword_cache[issue_key] ||= find_or_create_keyword(issue_key, vocab)
        next unless keyword

        pending_tags << {
          keyword_id: keyword.id,
          tag_object_type: model_class.name,
          tag_object_id: tw_id
        }

        if pending_tags.size >= BATCH_SIZE
          flush_tags(pending_tags)
          pending_tags = []
        end
      end
    end

    flush_tags(pending_tags)

    Rails.logger.info("[BulkColdpIssueTagJob] Issues phase complete. #{keyword_cache.size} keywords")

    # Import typed duplicate tags when "duplicate name" was included
    if import_duplicates
      import_typed_duplicates(dataset_id)
      Rails.logger.info("[BulkColdpIssueTagJob] Duplicates phase complete")
    end
  end

  private

  def valid_id?(model_class, tw_id)
    if model_class == Otu
      @valid_otu_ids.include?(tw_id)
    else
      @valid_taxon_name_ids.include?(tw_id)
    end
  end

  # Bulk-insert tags without existence checks. Tags for each keyword
  # are deleted before the first insert via delete_tags_for_keyword.
  def flush_tags(pending_tags)
    if pending_tags.empty?
      Rails.logger.info("[BulkColdpIssueTagJob] flush_tags called with 0 pending")
      return
    end

    unique_tags = pending_tags.uniq { |t| [t[:keyword_id], t[:tag_object_type], t[:tag_object_id]] }

    now = Time.current

    rows = unique_tags.map do |t|
      {
        keyword_id: t[:keyword_id],
        tag_object_type: t[:tag_object_type],
        tag_object_id: t[:tag_object_id],
        position: 999,
        project_id: @project_id,
        created_by_id: @user_id,
        updated_by_id: @user_id,
        created_at: now,
        updated_at: now
      }
    end

    Rails.logger.info("[BulkColdpIssueTagJob] flush_tags inserting #{rows.size} tags (from #{pending_tags.size} pending, #{pending_tags.size - unique_tags.size} dupes)")
    Tag.insert_all(rows)
  end

  def fetch_vocab
    response = Faraday.get(CLB_ISSUE_VOCAB_URL)
    JSON.parse(response.body)
  rescue => e
    raise TaxonWorks::Error, "Failed to fetch CLB issue vocabulary: #{e.message}"
  end

  def fetch_issues_tsv(dataset_id)
    base_url = Colrapi.base_url || 'https://api.checklistbank.org/'
    conn = Faraday.new(url: base_url) do |f|
      f.headers['Accept'] = 'text/tsv'
      f.options.timeout = 600
      f.adapter Faraday.default_adapter
    end

    response = conn.get("dataset/#{dataset_id}/issues")

    unless response.success?
      raise TaxonWorks::Error, "CLB issues endpoint returned status #{response.status}"
    end

    response.body
  end

  # Determine the TW model and ID from the COLDP source ID.
  #
  # Plain integer (e.g. "1000") → Otu, ID = 1000
  # Dashed ID (e.g. "1000-331311" or "10004-340318-hash") → TaxonName,
  #   ID = second segment (the TaxonName.id from Synonym.tsv nameID)
  def resolve_record(raw_id)
    parts = raw_id.to_s.strip.split('-')

    if parts.length == 1
      id = parts[0].to_i
      return nil unless id > 0
      [Otu, id]
    else
      id = parts[1].to_i
      return nil unless id > 0
      [TaxonName, id]
    end
  end

  def find_or_create_keyword(issue_key, vocab)
    entry = vocab.find { |v| v['name'] == issue_key }
    return nil unless entry

    keyword_name = "COLDP: #{titleize_issue(entry['name'])}"
    issue_uri = "https://api.checklistbank.org/vocab/issue##{entry['name'].tr(' ', '_')}"

    keyword = Keyword.find_or_create_by!(
      name: keyword_name,
      project_id: @project_id
    ) do |k|
      k.definition = entry['description'] || "CLB issue: #{entry['name']}"
      k.uri = issue_uri
      k.css_color = CLB_BRAND_COLOR
      k.by = @user_id
    end

    clear_tags_for_keyword(keyword)
    keyword
  end

  # Delete existing tags for a keyword on first encounter so we
  # can do clean insert_all without existence checks.
  # Only clears if the keyword has our CLB brand color and issue URI,
  # guarding against accidental deletion of user-created keywords.
  def clear_tags_for_keyword(keyword)
    return if @cleared_keyword_ids.include?(keyword.id)
    if keyword.css_color == CLB_BRAND_COLOR && keyword.uri.to_s.start_with?('https://api.checklistbank.org/vocab/issue')
      Tag.where(keyword_id: keyword.id).delete_all
    end
    @cleared_keyword_ids.add(keyword.id)
  end

  # Title-case an issue name, e.g. "synonym rank differs" → "Synonym Rank Differs"
  # Must match the issueTitle() function in IssuesTable.vue
  def titleize_issue(name)
    name.split(' ').map(&:capitalize).join(' ')
  end

