  def import_typed_duplicates(dataset_id)
    conn = clb_connection
    pending_tags = []
    total_usages = 0
    skipped_blank = 0
    skipped_zero = 0
    skipped_invalid = 0

    DUPLICATE_PRESETS.each do |preset|
      keyword = find_or_create_duplicate_keyword(preset)
      preset_count = 0
      offset = 0
      limit = 100

      loop do
        response = conn.get("dataset/#{dataset_id}/duplicate", preset[:params].merge(offset: offset, limit: limit))

        unless response.success?
          Rails.logger.warn("[BulkColdpIssueTagJob] Duplicate endpoint returned #{response.status} for preset #{preset[:id]}")
          break
        end

        data = JSON.parse(response.body)
        groups = data['result'] || []

        if groups.empty?
          Rails.logger.info("[BulkColdpIssueTagJob] No groups for preset #{preset[:id]} at offset #{offset}")
          break
        end

        # Log first few raw usages from first group of first preset
        if offset == 0 && preset == DUPLICATE_PRESETS.first && groups.first
          sample = groups.first['usages']&.first(2)&.map { |uw| uw['usage']&.slice('id', 'status') }
          Rails.logger.info("[BulkColdpIssueTagJob] Sample usages: #{sample.inspect}")
        end

        groups.each do |group|
          (group['usages'] || []).each do |usage_wrapper|
            usage = usage_wrapper['usage'] || {}
            usage_id = usage['id']
            status = usage['status']
            total_usages += 1

            if usage_id.blank?
              skipped_blank += 1
              next
            end

            model_class = (status == 'accepted') ? Otu : TaxonName
            tw_id = usage_id.to_i

            unless tw_id > 0
              skipped_zero += 1
              next
            end

            unless valid_id?(model_class, tw_id)
              skipped_invalid += 1
              next
            end

            preset_count += 1
            pending_tags << {
              keyword_id: keyword.id,
              tag_object_type: model_class.name,
              tag_object_id: tw_id
            }

            if pending_tags.size >= BATCH_SIZE
              flush_tags(pending_tags)
              pending_tags = []
            end
          end
        end

        break if data['last'] || groups.length < limit
        offset += limit
      end

      Rails.logger.info("[BulkColdpIssueTagJob] Preset #{preset[:id]} (#{preset[:text]}): #{preset_count} tags queued, keyword_id=#{keyword.id}")
    end

    Rails.logger.info("[BulkColdpIssueTagJob] Duplicates summary: #{total_usages} usages seen, #{pending_tags.size} pending, skipped: blank=#{skipped_blank} zero=#{skipped_zero} invalid=#{skipped_invalid}")
    flush_tags(pending_tags)
    Rails.logger.info("[BulkColdpIssueTagJob] Duplicates flush complete")
  end

  def find_or_create_duplicate_keyword(preset)
    keyword_name = "COLDP: Duplicate #{preset[:text]}"

    keyword = Keyword.find_or_create_by!(
      name: keyword_name,
      project_id: @project_id
    ) do |k|
      k.definition = "CLB duplicate detection: #{preset[:text]}. Records identified by ChecklistBank as potential duplicates within this category."
      k.uri = "https://api.checklistbank.org/vocab/issue#duplicate_#{preset[:id]}"
      k.css_color = CLB_BRAND_COLOR
      k.by = @user_id
    end

    clear_tags_for_keyword(keyword)
    keyword
  end

  def clb_connection
    base_url = Colrapi.base_url || 'https://api.checklistbank.org/'
    Faraday.new(url: base_url, request: { params_encoder: Faraday::FlatParamsEncoder }) do |f|
      f.headers['Accept'] = 'application/json'
      f.options.timeout = 30
      f.adapter Faraday.default_adapter
    end
  end
